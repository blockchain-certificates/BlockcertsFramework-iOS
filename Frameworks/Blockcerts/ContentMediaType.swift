public enum ContentMediaType : String {
    case textHtml = "text/html"
    case imagePng = "image/png"
    case imageJpeg = "image/jpeg"
    case imageGif = "image/gif"
    case imageBmp = "image/bmp"
    case applicationPdf = "application/pdf"
}
